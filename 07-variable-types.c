#include <stdio.h>

int main()
{
 // Integer variables
 int a = 5;
 printf("The value of a is %d", a);

 // Real variables
 // int b = 7.7; (7.7 is not an integer, It is a float)
 float b = 7.7;
 printf("\nThe value of b is %f", b);

 // Character variables
 char c = 'C';
 printf("\nMy name is %c", c);
 return 0;
}